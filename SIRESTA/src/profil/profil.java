    private void bprofilMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_bprofilMouseClicked
        // TODO add your handling code here:
        JnaFileChooser fileChooser = new JnaFileChooser();
        Window Window = null;
        boolean action = fileChooser.showOpenDialog(Window);
        if (action) {
            System.out.println(fileChooser.getSelectedFile());
            File selectedFile = fileChooser.getSelectedFile();
            String path = selectedFile.getAbsolutePath();
            txtprof.setText(path);
            
            try{
                byte[] img = Files.readAllBytes(selectedFile.toPath());
                ImageIcon imageIcon = new ImageIcon(img);
                
                int labelWidht = 160;
                int labelHeight = 170;
                
                int imageWidht = imageIcon.getIconWidth();
                int imageHeight = imageIcon.getIconHeight();
                
                double scaleX =  (double) labelWidht / (double) imageWidht;
                double scaleY =  (double) labelHeight / (double) imageHeight;
                double scale = Math.min(scaleY, scaleY);
                
                Image scaledImage = imageIcon.getImage().getScaledInstance((int) (scale * imageWidht), (int) (scale * imageHeight), Image.SCALE_SMOOTH);
                
               profile.setIcon(new ImageIcon(scaledImage));
            }catch (IOException ex){
                ex.printStackTrace();
            }
        }
    }//GEN-LAST:event_bprofilMouseClicked

